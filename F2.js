// 1.soma

let a = 10 ;
let b = 12 ;
let c = 15 ;

let soma = (a - b) + c;
console.log("O resultado da soma é: " + soma) ;

// 2. divisão

let d = 10 ;
let e = 12 ;
let f = 15 ;
let divisao = ((d - e) * (f + e)) / 3;
console.log("O resultado da divisao é: " + divisao) ;

// 3. soma dos dois

let forgeted = (soma + divisao) ;
console.log ("O resultado da soma é:" + forgeted) ;

// 4. multiplicacao

let g = 10;
let h = 12;
let i = 15;
let multiplicacao = ((g + h * i) - (i - g)) * 5;
console.log("O resultado da multiplicacao é: " + multiplicacao) ;

// 5. divisão das variáveis

let divisor = (multiplicacao / soma)
console.log ("O resultado do dividor é: " + divisor) ;