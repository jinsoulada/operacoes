// atribuição simples (=) 
let numero = 10;
console.log("atribuição simples: =", numero);

// atribuição de adição (++)
numero += 5;
console.log("Após atribuição de adição (+=5): numero = ", numero);

//atribuição de subtração (-=)
numero -= 3;
console.log("Após a atribuição de subtração (-=3): numero = ",  numero)

//atribuição de multiplicação (*=)
numero *= 2;
console.log("Após a atribuição de multiplicação (*=2): numero = ",  numero)

//atribuição de divisão (/=)
numero /= 4;
console.log("Após a atribuição de divisão (/=4): numero = ",  numero)